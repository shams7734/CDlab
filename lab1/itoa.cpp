#include<bits/stdc++.h>
using namespace std;
int main()
{
	int num,i,j;
	int q,r,len;
	int arr[10];
	string s;
	printf("Enter any number\n");
	scanf("%d",&num);
	r=num;
	i=0;len=0;
	while(r!=0)
	{
		q=r%10;
		r=r/10;
		arr[i]=q;
		i++;
		len++;
	}
	
	for(j=len-1;j>=0;j--)
	{
		printf("%c",arr[j]+48);
	}
	printf("\n");
}
